                <h3>Photography, YouTube and Fine-Art ...</h3>
                    <p>
                    Honestly I really thought all this pandemic fear-living would be over by now. I am starting to seriously wonder how the human race survived this long alongside germs or whether this fear-demic is now (even more so) politically driven by the ever-growing progressive movement in America. We still have the left campaigning on vaccines and face-masks when the right is, for the most-part, polar opposite. Which is more accurately representing the truth of the situation I honestly don't know. Frankly the politics over this "pandemic" scare me more than germs themselves because of the sociological and mental affects.  
                    <br /><br />
                    So, with that said, I have decided to once again slow my re-entry into events. It's not the fear of getting sick, it's the fear of misguiding authority and the rampant abuse of the power of politics. I don't want to be part of this progressive-sociological experiment and simply refuse to partake. I will find another way with less manipulation, bias and emotional conditioning to connect with my audience until these crazy times pass. Every storm eventually comes to an end .. right. 
                    <br /><br />
                    I am also pumping the brakes a little on the YouTube channel. Only going to be creating 1 video a week but also include a Field Note on the same topic and post over on the blog. For the most part it's an issue of finding the time right now, and with the Holidays fast approaching there will be even less free-time hanging around. Hopefully by March 2022 I can get back to the 2 videos per week again. 
