            <h4>
            Photography has always had a hidden crush on James, a self-taught explorer in the field of landscape photography. He admires mother nature from a distance, capturing her beauty and emotions through his photography as art.
            </h4>

            <h4 class="martop20">
            His more popular pieces include, "Once Afloat", "Avalon", "Between The Hills", "Bonsai Zen, "Lost Trail" and "Never Ending Story". Framed editions of these pieces can be viewed at many of his <a href="/exhibits">pop-up exhibits</a> which include locally-owned businesses around the locations he photographs.
            </h4>

            <ul
                class="timeline">

            <li
                class="timeline-date">1973</li>
            <li 
                class="timeline-history">
            <p>
            Born in Concord, a small, historical, suburb outside Boston, Massachusetts, James explored the rich history of the makings of America during his youth.  He learned to appreciate the beauty and stories that are shared by the landscape around us. From as far East as Boston where America's freedom was founded when Paul Revere gallopped over cobble-stone rock on his candle-lit "midnight ride" yelling, "The Brittish are coming!" to as far-away West into the Gold Rush of the Sierra Nevada Mountains.
            </p>
            </li>
            </ul>

            <ul
                class="timeline">
            <li
                class="timeline-date">1985</li>
            <li 
                class="timeline-history">
            <p>
            Twelve years later, his parents sold the east coast homestead and packed everyone into a white Subaru wagon -- a traveling cat named "buffy" separated him and his younger sister, Stacey, in the backseat -- and they journeyed 2,977 miles across America to began a new adventure in a small Northern California town at the crossroads of Highway 49 and Interstate 80 known as <a href="https://www.sierranevadageotourism.org/content/city-of-auburn-no-404-california-historical-landmark/sied6970ba03ce993f0d" target="_new">"North Fork Dry Diggins"</a>, or Auburn, as it was renamed in 1849.
            </p>
            </li>
            </ul>

            <ul class="timeline">
                <li class="timeline-date">1990</li>
                <li class="timeline-history">
                    <p>
                    While on a northwest college road tour with his mother he was shooting a foggy dock on a lake somewhere in the boonedocks outside of Eugene, Oregon with a hand-me-down Minolta SLR 35mm camera that was given to him by his 8th grade Journalism teacher, Mrs. Anderson. This was the moment James heard his calling, inspiring him to pursue a career in Journalism.
                    <!-- , but didn't fall in love for another 18 years. -->
                    </p>
                </li>
            </ul>

            <ul class="timeline">
                <li class="timeline-date">2012</li>
                <li class="timeline-history">
                    <p>
                    As they say, "time flies" and "life happens". James graduated from the University of Nevada, Reno in 1994 with a Bachelors degree in Liberal Arts, was blessed with three beautiful daughters and in 2012 moved back to the Sacramento area where he was reunited with long time friend, and fellow fine-art photographer, <a href="http://steveroyjr.com/" target="_new">Steve Roy</a>. Steve inspired James to pick-up his camera, a Nikon 1 V1 at the time, and start looking through the lens again. It wasn't long before James upgraded to a Nikon D5200 then a few months after that a full-frame Nikon D600. He often found himself and Steve road-tripping through Napa to the Pacific Ocean, shooting up and down the Northern California Coast.</p>
                    <p>With Steve's encouragement, he launched a website showcasing his work, which at that time was primarily black and white photography.
                    </p>
                </li>
            </ul>

            <ul class="timeline">
                <li class="timeline-date">2015<br />| <span class="timeline-small">thru</span><br />2017</li>
                <li class="timeline-history">
                    <p>
                    After moving back to the Reno/Tahoe area in 2015, James met his better-half, Michele, who after seeing his work, inspired him to  publicly display and sell his photographs.
                    </p>
            
                    <p>
                    In June of 2016 his first photos, printed on metal, were displayed at a local tap-house, Beer NV, in Reno, Nevada. Soon after was his first sale, Between The Hills, then a gallery showing in Carson City, Nevada at Brewery Arts Center.</p>
                    
                    <p>In early 2016, James started crafting his own frames from dark Walnut and continued showing and selling new artwork through out the year. 
                    </p>

                    <p>
                    In summer of 2017, James relocated to Las Vegas, Nevada. 
                    </p>
                </li>
            </ul>
                        
