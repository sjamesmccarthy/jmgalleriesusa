        </p>

        <p>
        Limited Edition Premium Designer Frames are hand-made from solid wood and depending on the size print size are either one-in-a-half or two-in-a-half inches wide. They have a Black, Whiskey or Dark Walnut custom surface. If a paper based print is being framed it will also include Tru Vue&reg; Museum Glass to protect your artwork. For an additional cost, Limited Edition Premiuming framing may also include a 4-ply archvial mat, or linen liner. However this may require a larger frame.
        </p>

